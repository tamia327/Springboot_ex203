public class Employee {

        private long SSN;
        private long DOB;

        public long getSSN() {
            return SSN;
        }

        public void setSSN(long SSN) {
            this.SSN = SSN;
        }

        public long getDOB() {
            return DOB;
        }

        public void setDOB(long DOB) {
            this.DOB = DOB;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        private String name;
}
